// Function to parse the markdown content into a structured array of questions
const parseQuestions = (markdown) => {
  const questions = [];
  const sections = markdown.split('### MCQ Questions from ');

  sections.forEach(section => {
    if (!section.trim()) return;

    const lines = section.split('\n').map(line => line.trim()).filter(line => line.length > 0);
    let currentQuestion = null;

    lines.forEach(line => {
      // Check for question number (e.g., "1.")
      const questionMatch = line.match(/^(\d+)\.\s*(.*)/);
      if (questionMatch) {
        if (currentQuestion) {
          questions.push(currentQuestion);
        }
        currentQuestion = {
          id: parseInt(questionMatch[1]),
          question: questionMatch[2],
          options: [],
          correctAnswer: '', // This needs to be manually added or derived if available
        };
      } else if (currentQuestion) {
        // Check for options (e.g., "* A) Option text")
        const optionMatch = line.match(/^\*\s*([A-D])\)\s*(.*)/);
        if (optionMatch) {
          currentQuestion.options.push({
            label: optionMatch[1],
            text: optionMatch[2],
          });
          // For simplicity, let's assume the correct answer is not explicitly in the markdown
          // In a real scenario, you'd have a separate answer key or a way to mark correct options.
          // For this quiz, we'll just show results based on user selection.
        }
      }
    });

    if (currentQuestion) {
      questions.push(currentQuestion);
    }
  });
  return questions;
};

